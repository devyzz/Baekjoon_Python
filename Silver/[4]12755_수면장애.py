# 수면 장애를 가진 강민이는 잠이 오지 않아 적잖은 고통을 느끼고 있다. 강민이는 잠이 오지 않을 때마다 속으로 양을 세고 있었는데, 오늘따라 백만 마리까지 세었는데도 잠이 오지 않았다.
# 한계를 느낀 강민이는 새로운 방법으로 수를 세기로 했다.
#
# 1부터 숫자를 쭉 이어 붙이면 다음과 같은 숫자열이 생성된다.
# 12345678910111213...
#
# 강민이는 이 숫자열을 한 숫자씩 떼어서 읽기 시작했다. 수면에 성공한 강민이는 다음날 일어나자마자 "
# N번째 숫자까지 읽었어!" 라고 기분 좋게 외쳤다.
# N번째 숫자는 무엇일까?

n = int(input())

digit = 1         # 현재 자릿수
count = 9         # 자릿수에 해당하는 숫자의 개수
start = 1         # 현재 자릿수의 시작 숫자

# n번째 숫자가 몇 자리 수 안에 있는지 찾기
while n > digit * count:
    n -= digit * count
    digit += 1
    count *= 10
    start *= 10

# 해당 자릿수의 숫자 중 몇 번째 숫자인지 계산
number = start + (n - 1) // digit

# 그 숫자의 몇 번째 자리인지 인덱스로 찾기
index = (n - 1) % digit
print(str(number)[index])


