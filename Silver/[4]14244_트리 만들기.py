# n과 m이 주어졌을 때, n개의 노드로 이루어져 있고, m개의 리프로 이루어져 있는 트리를 만드는 프로그램을 작성하시오.
# 항상 정답이 존재하는 경우만 입력으로 주어진다.
# 트리는 사이클이 없는 연결 그래프이고, 리프는 차수가 1인 노드를 의미한다.

N, L = map(int, input().split())

# 기본 체인에 사용할 노드의 수 (최소 체인 길이는 간선 1, 즉 2개의 노드가 필요)
k = N - L + 1

edges = []

# 기본 체인 구성: 0부터 k-1까지 일렬로 연결
for i in range(k - 1):
    edges.append((i, i + 1))

# 체인의 마지막 노드(k-1)에 나머지 노드들을 리프로 붙임
for i in range(k, N):
    edges.append((k - 1, i))

# 결과 출력
for a, b in edges:
    print(a, b)
